CREATE TABLE EMPLOYEE( 
    Id NUMBER(10) PRIMARY KEY, 
    Name VARCHAR(20), 
    Address VARCHAR(30),
    Phone_Number NUMBER(10), 
    Dept_No NUMBER(10), 
    Dept_Name VARCHAR(10), 
    Salary NUMBER(10), 
)

DESC EMPLOYEE;

